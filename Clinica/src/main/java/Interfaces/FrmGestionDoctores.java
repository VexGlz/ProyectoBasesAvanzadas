    private void actualizarTablaDoctores() {
        List<Doctor> doctores = doctorDAO.listar();
        DefaultTableModel modelo = new DefaultTableModel(
                new Object[]{"ID", "Nombre", "Apellido", "Correo", "Teléfono", "Especialidad"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column != 0; 
            }
        };
        for (Doctor d : doctores) {
            modelo.addRow(new Object[]{
                d.getId(),
                d.getNombre(),
                d.getApellido(),
                d.getCorreo(),
                d.getTelefono(),
                d.getEspecialidad()
            });
        }
        TbDoctores.setModel(modelo);
    }

