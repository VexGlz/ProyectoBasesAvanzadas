    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = TbDoctores.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un doctor de la tabla para eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este doctor?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            int idDoctor = (int) TbDoctores.getValueAt(filaSeleccionada, 0);
            boolean exito = doctorDAO.eliminar(idDoctor);
            if (exito) {
                JOptionPane.showMessageDialog(this, "Doctor eliminado correctamente.");
                actualizarTablaDoctores();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar el doctor.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int filaSeleccionada = TbDoctores.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un doctor de la tabla para actualizar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int id = (int) TbDoctores.getValueAt(filaSeleccionada, 0);
            String nombre = TbDoctores.getValueAt(filaSeleccionada, 1).toString().trim();
            String apellido = TbDoctores.getValueAt(filaSeleccionada, 2).toString().trim();
            String correo = TbDoctores.getValueAt(filaSeleccionada, 3).toString().trim();
            int telefono = Integer.parseInt(TbDoctores.getValueAt(filaSeleccionada, 4).toString().trim());
            String especialidad = TbDoctores.getValueAt(filaSeleccionada, 5).toString().trim();

            if (nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty() || especialidad.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Los campos no pueden estar vacíos.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Doctor doctor = new Doctor();
            doctor.setId(id);  
            doctor.setNombre(nombre);
            doctor.setApellido(apellido);
            doctor.setCorreo(correo);
            doctor.setTelefono(telefono);
            doctor.setEspecialidad(especialidad);

            boolean exito = doctorDAO.actualizar(doctor);

            if (exito) {
                JOptionPane.showMessageDialog(this, "Doctor actualizado correctamente.");
                actualizarTablaDoctores();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el doctor.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El teléfono debe ser un número válido.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

